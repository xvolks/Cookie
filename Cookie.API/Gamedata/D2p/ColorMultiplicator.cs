namespace Cookie.API.Gamedata.D2p
{
    public class ColorMultiplicator
    {
        // Fields
        public double Blue;

        public double Green;

        public double Red;

        // Methods
        public ColorMultiplicator(int red, int green, int blue)
        {
            Red = red;
            Green = green;
            Blue = blue;
        }
    }
}